"""
Этот модуль содержит главную функцию, которая выводит
все числа от 0 до 100.
"""

def main() -> None:
    """
    Главная функция, которая выводит все числа от 0 до 100.

    :rtype: None
    :return: None
    """

    for i in range(101):
        print(i)


if __name__ == '__main__':
    main()